      </p>
      <h2 id="five"> Sloth </h2>
      <img src="../Merch Images/Sloth.jpg"
          class="image2">
        <p class="one">
        Souls here practice the virtue of fortitude, opposite of sloth. Souls here just participate in strenuous exercise until they reach the next terrace.

</p>
      <h2 id="six"> Greed </h2>
      <img src="../Merch Images/greedpurg.jpg"
          class="image1">
        <p class="two">
