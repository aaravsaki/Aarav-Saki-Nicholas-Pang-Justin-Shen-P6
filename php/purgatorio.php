        <h2 id="one"> Ante-Purgatory </h2>
        <img src="../Merch Images/ante.jpg"
          class="image2">

      <p class="one" >
      Ante-Purgatory is the region below the entrance into Purgatory proper and houses two main categories of souls whose penitent Christian life was delayed or deficient: the excommunicate and the late-repentant. This transitional space parallels similar sections found in the Inferno (the space reserved for the lukewarm and the neutral angels found in Inferno) and in the Paradiso (the heavens under the shadow of Earth traversed by the pilgrim in Paradiso).
      </p >
      <hr class="line1" size="5%">
        <h2 id="two"> Pride </h2>
        <img src="../Merch Images/ego.jpg"
          class="image1">
      <p class="two">
      Souls here practice the virtue of humility, opposite of pride. They are forced to carry huge stones symbolizing their pride while climbing. The souls carry stones in order to force them to bow. Pride is the worst of the seven deadly sins, and therefore, it is the first terrace that a soul must overcome in its journey to Heaven.


      </p>
      <hr class="line1" size="5%">
        <h2 id="three"> Envy </h2>
        <img src="../Merch Images/Envy.jpg"
          class="image2">
      <p class="one">
      Souls in this terrace practice generosity, opposite of envy. They have their eyes sewn shut by fire while disembodied voices guide them up while talking about the great benefits of generosity. Envy is also related to jealousy which is often the root of self-loath and theft. Envy is often said to be the reason why Cain killed his brother Abel because he believed that Abel was more favored by God.

      </p>
      <hr class="line1" size="5%">
        <h2 id="four"> Wrath </h2>
        <img src="../Merch Images/wrathpurg.jpg"
          class="image1">
      <p class="two">
      Souls here practice the virtue of peace, opposite of wrath. This terrace is filled with an acrid mist darker than night that souls must path through to arrive at the next terrace. Unable to see the outside world with their eyes, the penitents experience hallucinatory visions in which they first "see" examples of meekness (the virtue opposite to wrath) and then "see" examples of wrath itself.


      </p>
      <hr class="line1" size="5%">
      <h2 id="five"> Sloth </h2>
      <img src="../Merch Images/Sloth.jpg"
          class="image2">
        <p class="one">
        Souls here practice the virtue of fortitude, opposite of sloth. People end up here due to lax love and pursuit of good. To correct themselves of this fault, the slothful now show great vigor in running around the terrace, shouting famous examples of slothful behavior and its contrary virtue (decisive zeal) as they go along.

          </p>
          <hr class="line1" size="5%">
      <h2 id="six"> Greed </h2>
      <img src="../Merch Images/greedpurg.jpg"
          class="image1">
        <p class="two">
          Souls here practice the value of justice, opposite of greed. Faces to the hard rock floor, they themselves call out the examples of greed and its opposing virtue (generosity). Pope Adrian V, who lived only a little more than a month after his election to the papacy in 1276 (19.103-5), explains how this prostrate position is fitting punishment for their neglect of spiritual matters and excessive attachment to worldly goods.


          </p>
          <hr class="line1" size="5%">
      <h2 id="seven"> Gluttony </h2>
      <img src="../Merch Images/gluttonypurg.jpg"
          class="image2">

        <p class="one">Souls here practice the value of temperance, opposite of gluttony. Here, souls climb as they are surrounded by food that is forever out of reach. The emaciated spirits (their eyes sunk so far back into their sockets the face resembles the letter M) suffer excruciating hunger and thirst.
Souls here include those of Forese Donati, Bonagiunta da Lucca, and Virgil's "Messianic" Eclogue.
          
        </p>
        <hr class="line1" size="5%" >
      <h2 id="eight"> Lust</h2>
      <img src="../Merch Images/lustpurg.jpg"
          class="image1">
      <p class="two">
      Souls here practice the value of Purity, opposite of lust. This is the final level of Purgatory before the Earthly Paradise. Examples of chastity and lust are provided by the penitents themselves as they walk within a raging fire on the seventh and final terrace of Purgatory. Souls here include Guido Guinizzelli, a poet, and Arnaut Daniel, also another poet.

        </p>
        <hr class="line1" size="5%">
      <h2 id="nine"> Earthly Paradise</h2>
      <img src="../Merch Images/earthly paradise.jpg"
          class="image2">
      <p class="one">
      At the summit of Mount Purgatory is the Earthly Paradise or Garden of Eden. Allegorically, it represents the state of innocence that existed before Adam and Eve fell from grace.
        </p>
