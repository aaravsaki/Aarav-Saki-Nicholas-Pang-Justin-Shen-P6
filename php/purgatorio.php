      <p class="one">
      Souls in this terrace practice generosity, opposite of envy. They have their eyes sewn shut while disembodied voices guide them up while talking about the great benefits of generosity.

      </p>
        <h2 id="four"> Wrath </h2>
        <img src="../Merch Images/wrathpurg.jpg"
          class="image1">
      <p class="two">
